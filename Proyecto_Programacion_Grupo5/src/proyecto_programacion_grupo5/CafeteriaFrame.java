    private void BotonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonModificarActionPerformed
        boolean yaTiene = true;
        boolean confirmacion = false;
        int empleado = 0;
        for (int i = 0; i < reservas.length; i++) {
            
            String id = botonIdEmpleado1.getText();
            String idExtraida = Integer.toString(empleados[i].getId());

            if (id.equals(idExtraida)) {

                if (reservas[i] == null) {
                    yaTiene = false;
                    empleado = i;
                    confirmacion=true;
                    JOptionPane.showMessageDialog(null, "El empleado " + empleados[empleado].getNombre() + " no tiene una reservacion");
                }

            }
        }
        for (int i = 0; i < reservas.length; i++) {
            String id = botonIdEmpleado1.getText();
            String idExtraida = Integer.toString(empleados[i].getId());

            if (id.equals(idExtraida)) {
                empleado=i;
                
                if (yaTiene == true) {
                    
                    reservas[empleado].setBebida(BotonOpciones.getSelectedItem().toString());
                    reservas[empleado].setHora(BotonHoras.getSelectedItem().toString());
                    JOptionPane.showMessageDialog(null, "Se modifico el pedido de: " + reservas[empleado].getNombre() + " por" + BotonOpciones.getSelectedItem().toString() + "y se entrega " + BotonHoras.getSelectedItem().toString());
                    confirmacion=true;
                }

            }

        }
        if (confirmacion==false) {
            JOptionPane.showMessageDialog(null, "El empleado no existe");
        }

    }//GEN-LAST:event_BotonModificarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String resultado="Estas son las reservas \n";
        for (int i = 0; i < reservas.length; i++) {
            if (reservas[i]!=null) {
                resultado+=reservas[i].getNombre()+"  ";
                resultado+=reservas[i].getBebida()+"  ";
                resultado+=reservas[i].getHora()+"  ";
                resultado+= "\n";
            }
        }
        JOptionPane.showMessageDialog(null, resultado);
    }//GEN-LAST:event_jButton1ActionPerformed

