    private void botonVerificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVerificar1ActionPerformed

        boolean confirmacion = false;
        boolean yaTiene = false;
        int empleado = 0;

        for (int i = 0; i < 6; i++) {

            String id = botonIdEmpleado1.getText();
            String idExtraida = Integer.toString(empleados[i].getId());

            if (id.equals(idExtraida)) {
                empleado = i;

                confirmacion = true;

            }

        }
        for (int i = 0; i < reservas.length; i++) {
            String id = botonIdEmpleado1.getText();
            String idExtraida = Integer.toString(empleados[i].getId());

            if (id.equals(idExtraida)) {
                if (reservas[empleado] != null) {
                    JOptionPane.showMessageDialog(null, "El empleado :" + reservas[empleado].getNombre() + " ya tiene una reserva");
                    yaTiene = true;
                }

                if (confirmacion == true && yaTiene == false) {
                    JOptionPane.showMessageDialog(null, "El empleado es: " + empleados[empleado].getNombre() + "\n" + "y reservo " + BotonOpciones.getSelectedItem() + " para ser entregado a las " + BotonHoras.getSelectedItem());
                    BaseDatos.reservarBebidas(reservas, empleados[empleado].getNombre(), BotonOpciones.getSelectedItem().toString(), BotonHoras.getSelectedItem().toString(), empleado);

                }
                
            }

        }
        if (confirmacion == false) {
            JOptionPane.showMessageDialog(null, "El empleado no existe");
        }
        



    }//GEN-LAST:event_botonVerificar1ActionPerformed

