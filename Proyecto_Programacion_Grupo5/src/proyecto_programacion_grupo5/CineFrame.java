    private void cargarComboSalas() {
    jComboBox1.removeAllItems();

    for (int i = 0; i < BaseDatos.totalSalas; i++) {
        Cine c = BaseDatos.salasCine[i];
        jComboBox1.addItem("Sala " + (i + 1) + " - " + c.pelicula + " (" + c.filas + "x" + c.columnas + ")");
    }

    if (BaseDatos.totalSalas > 0) {
        jComboBox1.setSelectedIndex(0);
        mostrarSala(0);
    }
}
    private void mostrarSala(int indice) {

    Cine sala = BaseDatos.salasCine[indice];

    String texto = "Película: " + sala.pelicula + "\n";
    texto += "Tamaño: " + sala.filas + "x" + sala.columnas + "\n\n";

    for (int i = 0; i < sala.filas; i++) {
        for (int j = 0; j < sala.columnas; j++) {
            int id = sala.getID(i, j);
            if (id == 0) {
                texto += "[  ] ";
            } else {
                texto += "[" + id + "] ";
            }
        }
        texto += "\n";
    }

    jTextArea1.setText(texto);
}


   

