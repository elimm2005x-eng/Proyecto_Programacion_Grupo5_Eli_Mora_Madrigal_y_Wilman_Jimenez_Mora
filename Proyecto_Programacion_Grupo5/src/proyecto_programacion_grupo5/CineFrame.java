    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int idx = jComboBox1.getSelectedIndex();
        Cine sala = BaseDatos.salasCine[idx];

        try {
            int fila = Integer.parseInt(JOptionPane.showInputDialog("Fila:")) - 1;
            int col = Integer.parseInt(JOptionPane.showInputDialog("Columna:")) - 1;
            int id = Integer.parseInt(JOptionPane.showInputDialog("ID del empleado:"));
            
            boolean existe = false;
            for (int i = 0; i < BaseDatos.totalEmpleados; i++) {
                if (BaseDatos.empleados[i].id == id) {
                    existe = true;
                    break;
                }
            }
            if (!existe) {
                JOptionPane.showMessageDialog(this, "Empleado no existe");
                return;
            }
            boolean ok = sala.reservar(fila, col, id);
            if (ok) {
                JOptionPane.showMessageDialog(this, "Reservado correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Asiento ocupado");
            }

            mostrarSala(idx);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Entrada inválida");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int idx = jComboBox1.getSelectedIndex();
        Cine sala = BaseDatos.salasCine[idx];

    try {
        int fila = Integer.parseInt(JOptionPane.showInputDialog("Fila:")) - 1;
        int col = Integer.parseInt(JOptionPane.showInputDialog("Columna:")) - 1;

        boolean ok = sala.liberar(fila, col);

        if (ok) {
            JOptionPane.showMessageDialog(this, "Asiento liberado");
        } else {
            JOptionPane.showMessageDialog(this, "El asiento ya estaba libre");
        }

        mostrarSala(idx);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Entrada inválida");
    }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int idx = jComboBox1.getSelectedIndex();

        Object[] opciones = {"25 (5x5)", "30 (5x6)"};

        int sel = JOptionPane.showOptionDialog(
            this,
            "Seleccione el tamaño de la sala:",
            "Cambiar tamaño",
            JOptionPane.DEFAULT_OPTION,
            JOptionPane.QUESTION_MESSAGE,
            null,
            opciones,
            opciones[0]
        );

        if (sel == 0) {
            BaseDatos.salasCine[idx].setTamaño(5, 5);
        } else if (sel == 1) {
            BaseDatos.salasCine[idx].setTamaño(5, 6);
        }

cargarComboSalas();


    }//GEN-LAST:event_jButton5ActionPerformed

